package com.bank.enums;

/**
 * Статус банковской карты.
 *
 * <p>Определяет текущее состояние карты и влияет на доступные операции
 * (платежи, переводы, блокировка).</p>
 */
public enum CardStatus {

    /**
     * Активная карта.
     * <p>Может использоваться для переводов и операций.</p>
     */
    ACTIVE,

    /**
     * Заблокированная карта.
     * <p>Операции запрещены. Может быть активирована администратором.</p>
     */
    BLOCKED,

    /**
     * Карта с истекшим сроком действия.
     * <p>Операции запрещены. Статус устанавливается автоматически
     * после окончания срока действия.</p>
     */
    EXPIRED
}
