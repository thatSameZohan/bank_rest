package com.bank.enums;

/**
 * Роль пользователя в системе.
 *
 * <p>Используется Spring Security для разграничения доступа
 * к защищённым ресурсам и операциям.</p>
 *
 * <p>Значения ролей должны начинаться с префикса {@code ROLE_},
 * так как это требование Spring Security.</p>
 */
public enum Role {

    /**
     * Обычный пользователь системы.
     *
     * <p>Может просматривать свои карты, выполнять переводы
     * между своими картами</p>
     */
    ROLE_USER,

    /**
     * Администратор системы.
     *
     * <p>Имеет полный доступ к управлению пользователями
     * и банковскими картами (создание, блокировка, удаление).</p>
     */
    ROLE_ADMIN
}
